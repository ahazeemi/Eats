package com.example.lenovo.eats.ClassModel;

public class TDBPair
{
    String first, second;

    public TDBPair()
    {

    }

    public TDBPair(String first, String second)
    {
        this.first = first;
        this.second = second;
    }

    public String getFirst()
    {
        return first;
    }

    public void setFirst(String first)
    {
        this.first = first;
    }

    public String getSecond()
    {
        return second;
    }

    public void setSecond(String second)
    {
        this.second = second;
    }
}
